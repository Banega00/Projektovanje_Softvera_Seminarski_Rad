    public OrderMealsForm(java.awt.Frame parent, boolean modal, MealOffer mo) {
        super(parent, modal);
        initComponents();
        mealOffer = mo;

        orderedMeals = new Meal[]{null, null, null, null};
        comboBoxesArray = new JComboBox[]{jComboBox1, jComboBox2, jComboBox3, jComboBox4};
        textFieldsArray = new JTextField[]{jTextField1, jTextField2, jTextField3, jTextField4};

        try {
            this.order = Controller.getInstance().getOrderedMealsForEmployee(mealOffer);
            fillComboBoxes();

            for (Meal m : order.getOrderedMeals()) {
                switch (m.getType()) {
                    case PREDJELO:
                        orderedMeals[0] = m;
                        jComboBox1.setSelectedItem(m);
                        jTextField1.setText(String.valueOf(m.getNumberOfPortions()));
                        break;
                    case GLAVNO_JELO:
                        orderedMeals[1] = m;
                        jComboBox2.setSelectedItem(m);
                        jTextField2.setText(String.valueOf(m.getNumberOfPortions()));
                        break;
                    case SALATA:
                        orderedMeals[2] = m;
                        jComboBox3.setSelectedItem(m);
                        jTextField3.setText(String.valueOf(m.getNumberOfPortions()));
                        break;
                    case DEZERT:
                        orderedMeals[3] = m;
                        jComboBox4.setSelectedItem(m);
                        jTextField4.setText(String.valueOf(m.getNumberOfPortions()));
                        break;
                }
            }
        } catch (Exception ex) {
            Logger.getLogger(OrderMealsForm.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Greška prilikom učitavanja poručenih jela");
            this.dispose();
        }

        setVisible(true);
    }

