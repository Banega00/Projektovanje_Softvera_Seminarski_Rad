    private void btnSaveOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveOrderActionPerformed
        for (int i = 0; i < orderedMeals.length; i++) {
            if (comboBoxesArray[i].getSelectedItem() != null) {
                orderedMeals[i] = (Meal) comboBoxesArray[i].getSelectedItem();
                try {
                    orderedMeals[i].setNumberOfPortions(Integer.parseInt(textFieldsArray[i].getText().trim()));
                    if(orderedMeals[i].getNumberOfPortions() <= 0) orderedMeals[i] = null;
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Broj porcija mora biti ceo broj");
                    return;
                }
            }
            if(comboBoxesArray[i].getSelectedItem() == null) orderedMeals[i] = null;
        }
        List<Meal> orderedMealsList = Arrays.asList(orderedMeals);
        try {
            Controller.getInstance().saveOrder(new Order(mealOffer, Controller.getInstance().getUserAccount(), orderedMealsList));
            JOptionPane.showMessageDialog(this, "Porudžbina je uspešno sačuvana");
            this.dispose();
        } catch (Exception ex) {
            Logger.getLogger(OrderMealsForm.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Greška prilikom čuvanja porudžbine");
        }
    }//GEN-LAST:event_btnSaveOrderActionPerformed

